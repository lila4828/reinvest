from crewai import Task

class AnalysisTask:
    def report_writing_task(self, agent, company_name, accounting_data, macro_data, context_tasks):
        return Task(
            description=f'''
            당신은 최종 결정권자인 수석 애널리스트입니다. 
            아래 제공된 [거시경제 데이터]와 [재무 채점 데이터], 그리고 리서치 에이전트가 수집한 [최신 뉴스 컨텍스트]를 모두 융합하여 {company_name}에 대한 최종 투자 리포트를 작성하세요.

            [입력 데이터]
            1. 거시경제 시황(Macro): \n{macro_data}\n
            2. 재무 팩트(Accounting): \n{accounting_data}\n
            3. 최신 뉴스: (리서치 에이전트의 결과물 참조)

            💡 [작성 엄수 규칙 및 리포트 품격 향상 지침]
            - 전달받은 ROE, YoY, 이평선, 현금흐름 패턴 등 숫자를 임의로 수정하거나 누락하지 마세요.
            - 🚨 숫자 가독성: PER, ROE 등 모든 지표는 소수점 둘째 자리에서 반올림하여 깔끔하게 작성하세요. (예: 5.378128 -> 5.38)
            - 🚨 화폐 단위: 매출이나 순이익, FCF 같은 큰 숫자는 0을 나열하지 말고 '조' 또는 '억' 단위로 간결하게 변환하여 표현하세요. (예: 333조 6,059억 원)
            - 🚨 PBR 보정: 만약 제공된 PBR이 N/A라면 억지로 표기하지 말고 생략하거나, 장부가치 대비 현재 주가 수준을 다른 지표로 대체하여 논리적으로 설명하세요.
            - 논리적 연결: 근거 없는 추측은 배제하고, 제공된 팩트들의 인과관계를 논리적으로 연결하세요. (예: 환율 하락 + 수출 기업 = 부정적 또는 긍정적 영향 분석)
            - 최종 투자 의견은 [Strong Buy / Buy / Hold / Sell] 중 하나로 명확히 제시하세요.

            [최종 리포트 마크다운 양식]
            # 📈 {company_name} 심층 투자 전략 리포트
            **최종 투자 의견:** [Strong Buy / Buy / Hold / Sell]

            ## 1. 🌍 매크로 및 시장 환경
            (거시경제 데이터와 VIX 지수를 바탕으로 현재 시장이 이 종목에 우호적인지 분석)

            ## 2. 📊 펀더멘털 및 퀀트 분석
            (회계 데이터의 수익성, 현금흐름, 단기 성장성(YoY), 장기 이평선, 재무 건전성을 종합 평가)

            ## 3. 📰 비즈니스 모멘텀 (최신 뉴스)
            (리서치팀이 가져온 핵심 뉴스를 기반으로 단기 주가 촉매제 분석)

            ## 4. 💡 수석 애널리스트 종합 결론
            (위 1, 2, 3을 종합하여 이 주식을 지금 매수/매도/관망해야 하는지에 대한 명확한 전략과 논리 제시)
            ''',
            expected_output=f'{company_name}에 대한 가독성이 뛰어나고 완벽한 4단 구조의 마크다운 형식 종합 투자 리포트',
            agent=agent,
            context=context_tasks # 리서치 에이전트가 수행한 Task를 문맥으로 연결
        )