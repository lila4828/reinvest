from crewai import Task

class YoutubeTask:
    def extract_guru_view(self, agent, company_name):
        return Task(
            description=f'''
            '주알홍쌤' 유튜브 채널을 검색하여 다음 지시를 수행하세요.

            1. 플랜 A: 먼저 채널 내에 '{company_name}'와 관련된 최근 영상이 있는지 검색하고, 있다면 그 영상의 핵심 논리를 추출하세요.
            2. 🚨 플랜 B [매우 중요]: 만약 '{company_name}'에 대한 개별 영상이 없다면, 주알홍쌤의 **가장 최근 시장 시황 분석 영상(시장 심리, 금리, 투자 마인드 등)**을 찾아서 요약하세요.
            3. 🚨 [추출 원칙]: 영상 스크립트는 매우 깁니다. 인사말 등 불필요한 내용은 버리되, 유튜버가 직접 발언한 **구체적인 수치(예: 30만 전자, 엔비디아 시총 비교 등), 독특한 비유(예: 에베레스트 고산병), 날것의 뷰**는 절대 축약하거나 점잖게 포장하지 마세요! 
            🚨 [환각 방지 엄수]: 검색된 영상 텍스트에 존재하지 않는 주가(예: 65,000원), 상승률(8%), 거래량 등의 가짜 수치를 절대 임의로 지어내지 마세요. 오직 유튜버가 실제 발언한 내용만 100% 그대로 인용해야 합니다.
            ※ 이 결과물은 최종 수석 애널리스트(AI)에게 전달될 원천 데이터입니다. 따라서 분석 에이전트가 유튜버의 의도를 100% 이해하고 리포트에 풍부하게 녹여낼 수 있도록, **개조식으로 짧게 치지 말고 분량 제한 없이 구체적인 서술형(줄글)으로 상세히 작성**하여 넘겨주세요.
            ''',
            expected_output='최종 분석 에이전트에게 전달하기 위한, 주알홍쌤의 생생한 투자 인사이트와 구체적 논리가 모두 보존된 상세한 서술형 보고서',
            agent=agent
        )